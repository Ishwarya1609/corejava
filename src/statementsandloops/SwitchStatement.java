package statementsandloops;

public class SwitchStatement {

	public static void main(String[] args) {
		int i=2;
		switch(i)
		{
		case 1:
			System.out.println("First Class");
			break;
		case 2:
			System.out.println("Second Class");
			break;
		case 3:
			System.out.println("Third class");
			break;
		}
		


	}

}
